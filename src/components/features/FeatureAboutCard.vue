const gravityScroll = () => {
  let start: number | null = null;
  let velocity = 0;
  const gravity = 2;
  const maxScroll = document.documentElement.scrollHeight - window.innerHeight;

  const animate = (timestamp: number) => {
    if (start === null) start = timestamp;

